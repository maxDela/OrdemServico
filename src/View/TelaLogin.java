    private void Status() {
        try {
            conexao = Conexao.conectar();
            if (conexao != null) {
                jLblStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Icos/dbok.png")));
            } else {
                jLblStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Icos/dberror.png")));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                conexao.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }

    public void Logar() {
        String sql = "Select * from tbusuario where login = ? and sanha = md5(?)";
        try {
            conexao = Conexao.conectar();
            pst = conexao.prepareStatement(sql);
            pst.setString(1, jTxtUsuario.getText());
            String captura = new String(jTxtUsuario.getText());
            pst.setString(2, jTextSenha.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                TelaPrincipal tela = new TelaPrincipal();
                TelaPrincipal.jLblUsuario.setText(rs.getString(2));
                tela.setVisible(true);
                this.dispose();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }

