    private void jBtnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAdicionarActionPerformed
      UsuarioModel obj = new UsuarioModel();

obj.setUsuario(jTxtUsuario.getText());
obj.setFone(jTxtFone.getText());
obj.setLogin(jTxtLogin.getText());
String captura = new String(jTxtSenha.getPassword());
obj.setSenha(captura);
obj.setPerfil(JCbxPerfil.getSelectedItem().toString());

if ((jTxtUsuario.getText().isEmpty()) || (jTxtFone.getText().isEmpty()) || (jTxtSenha.getPassword().length == 0) || (jTxtLogin.getText().isEmpty()) || JCbxPerfil.getSelectedItem().equals(" ")) {
JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigat√≥rios");
} else {
UsuarioDAO usuarioDAO = new UsuarioDAO();
usuarioDAO.adicionarUsuario(obj);

}
    }//GEN-LAST:event_jBtnAdicionarActionPerformed


